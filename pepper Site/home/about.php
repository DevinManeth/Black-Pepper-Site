        <h1>Black Pepper</h1>
        <p>  Black pepper is produced from the still-green, unripe drupe of the pepper plant. The drupes are cooked briefly in hot water, both to clean them and to prepare them for drying.The heat ruptures cell walls in the pepper, speeding the work of browning enzymes during drying.The drupes dry in the sun or by machine for several days, during which the pepper skin around the seed shrinks and darkens into a thin, wrinkled black layer. Once dry, the spice is called black peppercorn. On some estates, the berries are separated from the stem by hand and then sun-dried without boiling.

After the peppercorns are dried, pepper spirit and oil can be extracted from the berries by crushing them. Pepper spirit is used in many medicinal and beauty products. Pepper oil is also used as an ayurvedic massage oil and in certain beauty and herbal treatments.</p>

<h1>White pepper<h1>
<p>White pepper consists solely of the seed of the ripe fruit of the pepper plant, with the thin darker-coloured skin (flesh) of the fruit removed. This is usually accomplished by a process known as retting, where fully ripe red pepper berries are soaked in water for about a week so the flesh of the peppercorn softens and decomposes; rubbing then removes what remains of the fruit, and the naked seed is dried. Sometimes the outer layer is removed from the seed through other mechanical, chemical, or biological methods.

Ground white pepper is commonly used in Chinese, Thai, and Portuguese cuisines. It finds occasional use in other cuisines in salads, light-coloured sauces, and mashed potatoes as a substitute for black pepper, because black pepper would visibly stand out. However, white pepper lacks certain compounds present in the outer layer of the drupe, resulting in a different overall flavour.</p>
